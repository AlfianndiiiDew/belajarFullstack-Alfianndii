let i = Number(prompt("Masukkan angka antara 1-3:"));

switch (i) {
    case 1:
        console.log("Anda memasukkan angka Satu");
        break;
    case 2:
        console.log("Anda memasukkan angka Dua");
        break;
    case 3:
        console.log("Anda memasukkan angka Tiga");
        break;
    default:
        console.log("Angka yang Anda masukkan tidak antara 1-3");
}